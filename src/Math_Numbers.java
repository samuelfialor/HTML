public class Math_Numbers {
    public static void main(String[] args) {

        int myInt = 4;
        double mydouble = 6.6;

        System.out.println(myInt + mydouble);
        System.out.println( Math.abs(mydouble));
        System.out.println(Math.pow(myInt, 4));
        System.out.println(Math.sqrt(36));
        System.out.println(Math.min(9,3));
    }
}
